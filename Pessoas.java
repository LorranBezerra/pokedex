
public class Pessoas {

    private String nome;
    private String genero;
    private int idade;
    private String aniversario;
    private int telefone;
    private String hobby;
    private String comidaFavorita;
    private String coisasFavorita;
    private String coisasQueOdeia;
    private String observacao;
    private String mensagem;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getGenero() {
        return genero;
    }

    public void setGenero(String genero) {
        this.genero = genero;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public String getAniversario() {
        return aniversario;
    }

    public void setAniversario(String aniversario) {
        this.aniversario = aniversario;
    }

    public int getTelefone() {
        return telefone;
    }

    public void setTelefone(int telefone) {
        this.telefone = telefone;
    }

    public String getHobby() {
        return hobby;
    }

    public void setHobby(String hobby) {
        this.hobby = hobby;
    }

    public String getComidaFavorita() {
        return comidaFavorita;
    }

    public void setComidaFavorita(String comidaFavorita) {
        this.comidaFavorita = comidaFavorita;
    }

    public String getCoisasFavorita() {
        return coisasFavorita;
    }

    public void setCoisasFavorita(String coisasFavorita) {
        this.coisasFavorita = coisasFavorita;
    }

    public String getCoisasQueOdeia() {
        return coisasQueOdeia;
    }

    public void setCoisasQueOdeia(String coisasQueOdeia) {
        this.coisasQueOdeia = coisasQueOdeia;
    }

    public String getObservacao() {
        return observacao;
    }

    public void setObservacao(String observacao) {
        this.observacao = observacao;
    }

    public String getMensagem() {
        return mensagem;
    }

    public void setMensagem(String mensagem) {
        this.mensagem = mensagem;
    }

}
